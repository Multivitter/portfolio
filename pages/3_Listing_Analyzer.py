st.set_page_config(page_title="Listing Analyzer", page_icon="🔍", layout="wide")

st.title("🔍 Кейс: Amazon Listing Analyzer (продукт)")
st.caption("Не скрипт, а законченный продукт с ценой. Демо-данные.")

